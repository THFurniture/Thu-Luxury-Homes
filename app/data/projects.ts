export type Project = {
  slug: string;
  name: string;
  location: string;
  type: string;
  isShown: boolean;
  coverImage: string;
  images: string[];
};

type ProjectInput = Omit<Project, "coverImage" | "images" | "isShown"> & {
  folder: string;
  fileBase: string;
  imageCount: number;
  coverIndex?: number;
};

function buildImages(folder: string, fileBase: string, imageCount: number) {
  const publicFolder = folder
    .split("/")
    .map((segment) => encodeURIComponent(segment))
    .join("/");

  return Array.from(
    { length: imageCount },
    (_, index) => `/projects-imgs/${publicFolder}/${fileBase}-${index + 1}.avif`,
  );
}

function defineProject({
  folder,
  fileBase,
  imageCount,
  coverIndex = 1,
  ...project
}: ProjectInput): Project {
  const images = buildImages(folder, fileBase, imageCount);

  return {
    ...project,
    isShown: folder.startsWith("Shown/"),
    coverImage: images[Math.max(0, Math.min(coverIndex - 1, images.length - 1))],
    images,
  };
}

